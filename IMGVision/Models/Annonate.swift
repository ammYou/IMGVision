import Foundation

struct Annonate: Codable {
    let responses: [Response]?
}

struct Response: Codable {
    let webDetection: WebDetection?
    let safeSearchAnnotation: SafeSearchAnnotation?
    let imagePropertiesAnnotation: ImagePropertiesAnnotation?
    let cropHintsAnnotation: CropHintsAnnotation?
    let labelAnnotations: [LabelAnnotation]?
}

struct CropHintsAnnotation: Codable {
    let cropHints: [CropHint]?
}

struct CropHint: Codable {
    let importanceFraction: Int?
    let confidence: Double?
    let boundingPoly: BoundingPoly?
}

struct BoundingPoly: Codable {
    let vertices: [Vertex]?
}

struct Vertex: Codable {
    let x, y: Int?
}

struct ImagePropertiesAnnotation: Codable {
    let dominantColors: DominantColors?
}

struct DominantColors: Codable {
    let colors: [ColorElement]?
}

struct ColorElement: Codable {
    let score, pixelFraction: Double?
    let color: ColorColor?
}

struct ColorColor: Codable {
    let blue, green, red: Int?
}

struct LabelAnnotation: Codable {
    let description: String?
    let topicality, score: Double?
    let mid: String?
}

struct SafeSearchAnnotation: Codable {
    let adult, violence, spoof, racy: String?
    let medical: String?
}

struct WebDetection: Codable {
    let visuallySimilarImages, partialMatchingImages, fullMatchingImages: [Image]?
    let bestGuessLabels: [BestGuessLabel]?
    let pagesWithMatchingImages: [PagesWithMatchingImage]?
    let webEntities: [WebEntity]?
}

struct BestGuessLabel: Codable {
    let label: String?
}

struct Image: Codable {
    let url: String?
}

struct PagesWithMatchingImage: Codable {
    let fullMatchingImages: [Image]?
    let url, pageTitle: String?
    let partialMatchingImages: [Image]?
}

struct WebEntity: Codable {
    let description, entityID: String?
    let score: Double?
    
    enum CodingKeys: String, CodingKey {
        case description
        case entityID = "entityId"
        case score
    }
}

// MARK: Convenience initializers

extension Annonate {
    init(data: Data) throws {
        self = try JSONDecoder().decode(Annonate.self, from: data)
    }
    
    init(_ json: String, using encoding: String.Encoding = .utf8) throws {
        guard let data = json.data(using: encoding) else {
            throw NSError(domain: "JSONDecoding", code: 0, userInfo: nil)
        }
        try self.init(data: data)
    }
    
    init(fromURL url: URL) throws {
        try self.init(data: try Data(contentsOf: url))
    }
    
    func jsonData() throws -> Data {
        return try JSONEncoder().encode(self)
    }
    
    func jsonString(encoding: String.Encoding = .utf8) throws -> String? {
        return String(data: try self.jsonData(), encoding: encoding)
    }
}

